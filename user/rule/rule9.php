	<h2>幸运农场規則說明</h2>
	<ul>
		<li>每期幸运农场（<a class="label" href="http://168kai.com/Lottery/10010" target="_blank">開獎網</a>）開獎球數共八粒。每粒球除了總和玩法，其它都有單獨的投註頁面。幸运农场每天開84期，每期間隔10分鐘。投註時間為8分鐘，等待開獎時間為2分鐘，北京時間（GMT+8）每天白天從上午09：00開到晚上23：00。</li>
	</ul>
	<ul>
		<li><span class="label">1.兩面：</span>指單、雙；大、小、尾大、尾小。</li>
		<li class="stress">單、雙：號碼為雙數叫雙，如8、16；號碼為單數叫單，如19、5。</li>
		<li class="stress">大、小：開出之號碼大於或等於11為大，小於或等於10為小。</li>
		<li class="stress">尾大、尾小：開出之尾數大於或等於5為尾大，小於或等於4為尾小。</li>
		<li class="stress">每壹個號碼為壹投註組合，假如投註號碼為開獎號碼並在所投的球位置，視為中獎，其余情形視為不中獎。</li>
	</ul>
	<ul>
		<li><span class="label">2.中發白</span></li>
		<li class="stress">中：開出之號碼為01、02、03、04、05、06、07</li>
		<li class="stress">發：開出之號碼為08、09、10、11、12、13、14</li>
		<li class="stress">白：開出之號碼為15、16、17、18、19、20</li>
	</ul>
	<ul>
		<li><span class="label">3.方位</span></li>
		<li class="stress">東：開出之號碼為01、05、09、13、17</li>
		<li class="stress">南：開出之號碼為02、06、10、14、18</li>
		<li class="stress">西：開出之號碼為03、07、11、15、19</li>
		<li class="stress">北：開出之號碼為04、08、12、16、20</li>
	</ul>
	<ul>
		<li><span class="label">13.龍虎</span>以第壹球的中獎號碼和第八球的中獎號碼做為對獎號碼。</li>
		<li class="stress">龍：開出之號碼第壹球的中獎號碼大於第八球的中獎號碼。如 第壹球開出14 第八球開出09；第壹球開出17 第八球開出08；第壹球開出05 第八球開出01...中獎為龍。</li>
		<li class="stress">虎：開出之號碼第壹球的中獎號碼小於第八球的中獎號碼。如 第壹球開出14 第八球開出16；第壹球開出13 第八球開出18；第壹球開出05 第八球開出08...中獎為虎。</li>
	</ul>
	<ul>
		<li><span class="label">14.正碼</span></li>
		<li class="stress">從01至20中任意選擇1個號碼進行投註， 投註號碼與開獎號碼中任意1個位置的號碼相符，即中獎。</li>
	</ul>
	<ul>
		<li><span class="label">4.總和單雙：</span>所有8個開獎號碼的數字總和值是單數為總和單，如數字總和值是31、51；所有8個開獎號碼的數字總和值是雙數為總和雙，如數字總和是42、80；假如投註組合符合中獎結果，視為中獎，其余情形視為不中獎。</li>
		<li><span class="label">5.總和大小：</span>所有8個開獎號碼的數字總和值85到132為總大；所有8個開獎號碼的數字總和值36到83為總分小；所有8個開獎號碼的數字總和值為84打和；如開獎號碼為01、20、02、08、17、09、11，數字總和是68，則總分小。假如投註組合符合中獎結果，視為中獎，其余情形視為不中獎，打和不計算結果。</li>
		<li><span class="label">6.總尾大小：</span>所有8個開獎號碼的數字總和數值的個位數大於或等於5為總尾大，小於或等於4為總尾小；假如投註組合符合中獎結果，視為中獎，其余情形視為不中獎。</li>
		<li><span class="label">7.選二任選：</span>指從01至20中任意選擇2個號碼對開獎號碼中任意2個位置的投註。 投註號碼與開獎號碼中任意2個位置的號碼相符，即中獎。</li>
		<li><span class="label">8.選二連組：</span>指從01至20中任意選擇2個號碼對開獎號碼中按開獎順序出現的2個連續位置的投註。 投註號碼與開獎號碼中按開獎順序出現的2個連續位置的號碼相符（順序不限），即中獎。</li>
		<li><span class="label">9.選三任選：</span>指從01至20中任意選擇3個號碼對開獎號碼中任意3個位置的投註。 投註號碼與開獎號碼中任意3個位置的號碼相符，即中獎。</li>
		<li><span class="label">10.選三前組：</span>指從01至20中任意選擇3個號碼對開獎號碼中按開獎順序出現的前3個連續位置的投註。 投註號碼與開獎號碼中按開獎順序出現的前3個位置的號碼相符（順序不限），即中獎。</li>
		<li><span class="label">11.選四任選：</span>指從01至20中任意選擇4個號碼，對開獎號碼中任意4個位置的投註。 投註號碼與開獎號碼中任意4個位置的號碼相符，即中獎。</li>
		<li><span class="label">12.選五任選：</span>指從01至20中任意選擇5個號碼，對開獎號碼中任意5個位置的投註。 投註號碼與開獎號碼中任意5個位置的號碼相符，即中獎。</li>
	</ul>