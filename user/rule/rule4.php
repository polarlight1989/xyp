<h2>极速赛车規則說明</h2>
	<ul>
		<li>該遊戲的投註時間、開獎時間和開獎號碼與“极速赛车”完全同步，全天24小时開獎，每5分鐘開壹次獎，每天開獎288期。</li>
		<li>具體遊戲規則如下:</li>
	</ul>
	<ul>
		<li><span class="label">1～10 兩面：</span>指 單、雙；大、小。</li>
		<li class="stress"><span class="label">單、雙：</span>號碼為雙數叫雙，如4、8；號碼為單數叫單，如5、9。</li>
		<li class="stress"><span class="label">大、小：</span>開出之號碼大於或等於6為大，小於或等於5為小。</li>
		<li class="stress"><span class="label">第壹名～第十名 車號指定：</span>每壹個車號為壹投註組合，開獎結果“投註車號”對應所投名次視為中獎，其余情形視為不中獎。</li>
	</ul>
	<ul>
		<li><span class="label">1～5龍虎 </span></li>
		<li class="stress"><span class="label">冠&nbsp;&nbsp;軍 龍/虎：</span>“第壹名”車號大於“第十名”車號視為【龍】中獎、反之小於視為【虎】中獎，其余情形視為不中獎。</li>
		<li class="stress"><span class="label">亞&nbsp;&nbsp;軍 龍/虎：</span>“第二名”車號大於“第九名”車號視為【龍】中獎、反之小於視為【虎】中獎，其余情形視為不中獎。</li>
		<li class="stress"><span class="label">第三名 龍/虎：</span>“第三名”車號大於“第八名”車號視為【龍】中獎、反之小於視為【虎】中獎，其余情形視為不中獎。</li>
		<li class="stress"><span class="label">第四名 龍/虎：</span>“第四名”車號大於“第七名”車號視為【龍】中獎、反之小於視為【虎】中獎，其余情形視為不中獎。</li>
		<li class="stress"><span class="label">第五名 龍/虎：</span>“第五名”車號大於“第六名”車號視為【龍】中獎、反之小於視為【虎】中獎，其余情形視為不中獎。</li>
	</ul>
	<ul>
		<li><span class="label">冠軍車號＋亞軍車號＝冠亞和值（為3~19)</span></li>
		<li class="stress"><span class="label">冠亞和單雙：“冠亞和值”</span>為單視為投註“單”的註單視為中獎，為雙視為投註“雙”的註單視為中獎，其余視為不中獎。</li>
		<li class="stress"><span class="label">冠亞和大小：“冠亞和值”</span>大於11時投註“大”的註單視為中獎，小於或等於11時投註“小”的註單視為中獎，其余視為不中獎。</li>
		<li class="stress"><span class="label">冠亞和指定：“冠亞和值”</span>可能出現的結果為3～19， 投中對應<span class="label">“冠亞和值”</span>數字的視為中獎，其余視為不中獎。</li>
	</ul>
